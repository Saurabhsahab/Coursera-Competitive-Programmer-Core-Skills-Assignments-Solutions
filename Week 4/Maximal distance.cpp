void bt(ll si,ll ss,ll se){
	if(ss>se)
		return;

	if(ss==se){
		b[si].F=se;
		b[si].S=se;
		return;
	}

	ll m=(ss+se)/2;

	bt(2*si,ss,m);
	bt(2*si+1,m+1,se);

	if(a[b[2*si].F]>=a[b[2*si+1].F])
		b[si].F=b[2*si].F;
	else
		b[si].F=b[2*si+1].F;

	if(a[b[2*si].S]<=a[b[2*si+1].S])
		b[si].S=b[2*si].S;
	else
		b[si].S=b[2*si+1].S;

	return;
}

pair<ll,ll> qu(ll si,ll ss,ll se,ll l,ll r){
	if(ss>se)
		return mkp(0,1000000);
	if(ss>r||se<l)
		return mkp(0,1000000);

	if(ss>=l&&se<=r)
		return b[si];

	ll m=(ss+se)/2;

	pair<ll,ll> p=qu(2*si,ss,m,l,r);
	pair<ll,ll> q=qu(2*si+1,m+1,se,l,r);

    ll x,y;

    if(a[p.F]>=a[q.F])
		x=p.F;
	else
		x=q.F;

	if(a[p.S]<=a[q.S])
		y=p.S;
	else
		y=q.S;

	return mkp(x,y);
}



